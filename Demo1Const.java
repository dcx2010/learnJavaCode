

// 演示程序：常量
public class Demo1Const{
	public static void main(String[] args){
		// 字符串常量
		System.out.println("abcd");
		System.out.println("123");
		
		
		// 空字符
		System.out.println("");
		
		// 整数
		System.out.println(100);
		System.out.println(-100);
		
		// 打印布尔常量
		System.out.println(true);
		System.out.println(false);
		
		// 不能打印的示例：null、'ac'、''
		
	}
	
	
	
	
}